package Java_basics;

public class Comments {
    public static void main(String[] args) {
        //This starts with double slash(//) and ends with end of the line.
        System.out.println("This is single line comment");
        /*This starts with slash star(/*)and ends with star slash.
        This is multiline comment
         */
        System.out.println("This is Multi-line comment");

    }
}
